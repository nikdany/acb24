public class _03BinaryOperations {
    public static void main(String[] args) {
//adition
        int i = 15;
        int b = 30;
        int result = i + b;
        System.out.println(result);
//multiplying
        result = i * b;
        System.out.println(result);
//substracting
        result = i - b;
        System.out.println(result);
//dividing
        result = i/b;
        System.out.println(result);
//dividing with remainder
        result = i%5;
        System.out.println(result);
    }
}
